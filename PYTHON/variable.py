# Numerics data types in python
# int data type
var1 = 10
print(var1, type(var1))
# float data type
var2 = 20.0
print(var2, type(var2))
# Complex data type
var3 = 2 + 82j
print(var3, type(var3))

# String
var4 = "String"
print(var4, type(var4))
# Array
var5 = [10, 11, 2.1, "hello"]
print(var5, type(var5))